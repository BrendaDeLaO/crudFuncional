    public void listar() {
        String sql = "SELECT * FROM user";

        try {
            cn = con.getConexion();
            st = cn.createStatement();
            rs = st.executeQuery(sql);

            Object[] user = new Object[9];
            modelo = (DefaultTableModel) TablaDatos.getModel();
            while (rs.next()) {
                user[0] = rs.getInt("id_user");
                user[1] = rs.getString("name");
                user[2] = rs.getString("last_name");
                user[3] = rs.getString("email");
                user[4] = rs.getString("phone_number");
                user[5] = rs.getString("reason");
                user[6] = rs.getString("verification");
                user[7] = rs.getString("date");
                user[8] = rs.getString("state");
                modelo.addRow(user);
            }
        } catch (Exception e) {
            System.out.println("Error en el método listar: " + e);
        }
    }

    public void agregar() {
        String nombre = txtNOMBRE.getText();
        String apellido = txtAPELLIDO.getText();
        String correo = txtCORREO.getText();
        String celular = txtCELULAR.getText();
        String consulta = txtCONSULTA.getText();
        String verificacion = txtVERIF.getText();
        String fecha = txtFECHA.getText();
        String estado = txtESTADO.getText();

        if (nombre.equals("") || apellido.equals("") || correo.equals("") || celular.equals("") || consulta.equals("") || verificacion.equals("") || fecha.equals("") || estado.equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan valores");
        } else {
            String sql = "INSERT INTO user (name, last_name, email, phone_number, reason, verification, date, state) values ('" + nombre + "', '" + apellido + "', '" + correo + "', '" + celular + "','" + consulta + "','" + verificacion + "','" + fecha + "','" + estado + "')";
            try {
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);
                System.out.println("Registro Exitoso");
                limpiar();
            } catch (Exception e) {
                System.out.println("Error en el método agregar" + e);
            }
        }
    }

    public void modificar() {
        String nombre = txtNOMBRE.getText();
        String apellido = txtAPELLIDO.getText();
        String correo = txtCORREO.getText();
        String celular = txtCELULAR.getText();
        String consulta = txtCONSULTA.getText();
        String verificacion = txtVERIF.getText();
        String fecha = txtFECHA.getText();
        String estado = txtESTADO.getText();

        if (nombre.equals("") || apellido.equals("") || correo.equals("") || celular.equals("") || consulta.equals("") || verificacion.equals("") || fecha.equals("") || estado.equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan valores");
        } else {
            String sql = "UPDATE user SET name = '" + nombre + "', last_name = '" + apellido + "', email = '" + correo + "', phone_number = '" + celular + "', reason = '" + consulta + "', verification = '" + verificacion + "', date = '" + fecha + "', state = '" + estado + "' WHERE id_user = " + id;
            try {
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);
                System.out.println("Actualización Exitosa");
                limpiar();
            } catch (Exception e) {
                System.out.println("Error en el método modificar" + e);
            }
        }
    }

    public void eliminar() {
        int fila = TablaDatos.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada.");
            limpiar();
        } else {
            String sql = "DELETE FROM user WHERE id_user=" + id;
            try {
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Usuario correctamente eliminado.");
                limpiar();
            } catch (Exception e) {
                System.out.println("Error en el metodo eliminar: " + e);
            }
        }
    }

    public void limpiar() {
        for (int i = 0; i < TablaDatos.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

