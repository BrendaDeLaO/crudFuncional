    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int fila = TablaDatos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        } else {
            id = Integer.parseInt((String) TablaDatos.getValueAt(fila, 0).toString());
            String nombre = (String) TablaDatos.getValueAt(fila, 1);
            String apellido = (String) TablaDatos.getValueAt(fila, 2);
            String correo = (String) TablaDatos.getValueAt(fila, 3);
            String celular = (String) TablaDatos.getValueAt(fila, 4);
            String consulta = (String) TablaDatos.getValueAt(fila, 5);
            String verificacion = (String) TablaDatos.getValueAt(fila, 6);
            String fecha = (String) TablaDatos.getValueAt(fila, 7);
            String estado = (String) TablaDatos.getValueAt(fila, 8);

            txtID.setText("" + id);
            txtVERIF.setText(verificacion);
            txtFECHA.setText(fecha);
            txtESTADO.setText(estado);
            txtNOMBRE.setText(nombre);
            txtAPELLIDO.setText(apellido);
            txtCORREO.setText(correo);
            txtCELULAR.setText(celular);
            txtCONSULTA.setText(consulta);
        }
    }//GEN-LAST:event_TablaDatosMouseClicked

